# Operators

# Arethematic Operator
a = 10 
b = 2

print(a + b)
print(a - b)
print(a / b)
print(a * b)
print(a % b)
print(a ** b)
print("")

# Assignment Operator

num = 10

num += 6  
print(num)
num -= 6  
print(num)
num *= 6  
print(num)
num /= 6  
print(num)
num **= 6 
print(num)
num %= 6   
print(num)
print("")

# Comparison Operator
num1 = 10
num2 = 13
print(num1 == num2)
print(num1 != num2)
print(num1 > num2)
print(num1 < num2)
print(num1 >= num2)
print(num1 <= num2)
print("")

# Logical Operator

c = 12 

print(c % 2 == 0 or c < 10)

e = 10
f = 5 

print(e == f and e > f)
print(" ")




# Truth Table of 'or'
print("True or False is", True or False)
print("True or True is", True or True)
print("False or True is", False or True)
print("False or False is", False or False)
print(" ")

# Truth Table of 'and'

print("True and False is", True and False)
print("False and False is", False and False)
print("False and True is", False and True)
print("True and True is", True and True)
print(" ")

# Truth Table of not'

print(not(False))
print(not(True))